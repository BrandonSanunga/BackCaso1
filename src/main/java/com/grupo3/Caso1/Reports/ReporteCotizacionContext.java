package com.grupo3.Caso1.Reports;

public class ReporteCotizacionContext {
	private String marca;
	private String modelo;
	private Integer year;
	private String capacidad_carga;
	private String direccion;
	private Integer cilindros;
	private String motor;
	private String tecnologia;
	private Double precio;
	private String nombres_cliente;
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public String getCapacidad_carga() {
		return capacidad_carga;
	}
	public void setCapacidad_carga(String capacidad_carga) {
		this.capacidad_carga = capacidad_carga;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public Integer getCilindros() {
		return cilindros;
	}
	public void setCilindros(Integer cilindros) {
		this.cilindros = cilindros;
	}
	public String getMotor() {
		return motor;
	}
	public void setMotor(String motor) {
		this.motor = motor;
	}
	public String getTecnologia() {
		return tecnologia;
	}
	public void setTecnologia(String tecnologia) {
		this.tecnologia = tecnologia;
	}
	public Double getPrecio() {
		return precio;
	}
	public void setPrecio(Double precio) {
		this.precio = precio;
	}
	public String getNombres_cliente() {
		return nombres_cliente;
	}
	public void setNombres_cliente(String nombres_cliente) {
		this.nombres_cliente = nombres_cliente;
	}
	
	
	

}
